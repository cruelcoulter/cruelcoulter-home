                      <h2>Overview</h2><p>Co. G of the105th Regiment Ohio Volunteer Infantry was mustered in Aug. 20,   1862, at Camp Taylor in Cleveland, Ohio, by J.R. Paxton, Captain 15th Infantry.   It was mustered out June 3, 1865, near Washington D.C. by Jacob Kline, Captain   16th Infantry.</p><p> The 105th was part of the 2nd Brigade of the 3rd Division of the 14th Army   Corps, part of the Army of the Cumberland under Major Gen. William S. Rosecrans.   Brig. Gen. John M. Brannan commanded the 3rd Division. Col. John T. Croxton   commanded the 2nd Brigade until he was wounded and replaced by Col. William   H. Hays.</p><p> The 105th was involved in the battle of Perryville, Ky., in October 1862.   Many of the soldiers in Co. G were killed or injured at Murfreesboro between   Feb. and June 1863. One person from the company was captured and another injured   at Chickamauga in Sept. 1863; another was killed at Mission Ridge, Tenn., in   Nov. 1863; a few were killed in Georgia in 1864.</p>
