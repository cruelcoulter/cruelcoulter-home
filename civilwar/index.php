                        <p> One Hundred and Fifth Ohio Volunteer Infantry, 1013 men, Albert S. Hall colonel,   mustered in at Camp Taylor, near Cleveland, August 20-21, 1862; served in the   defense of Kentucky at Perryville during Bragg's invasion in 1862; in October   ordered into Tennessee; in January, 1863, assigned to the Fourth Division, Fourteenth   Army Corps. In active service in Tennessee through 1863 at Chickamauga and Mission   Ridge; took part in Sherman's great campaign through Georgia in 1864; in the   campaign of the Carolinas in the opening months of 1865; reviewed at Washington   May 24th, and there mustered out June 3, 1865, 427 men, Lieutenant Colonel George   T. Perkins commanding.</p>
